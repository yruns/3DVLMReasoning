@dataclass
class ParsingTestCase:
    """A query parsing test case."""

    query_id: str
    query: str
    expected_parse_mode: str
    expected_hypothesis_kind: str
    expected_target_categories: list[str]
    expected_anchor_categories: list[str] = field(default_factory=list)
    expected_relation: str | None = None
    expected_select_constraint_type: str | None = None
    description: str = ""


@dataclass
class KeyframeTestCase:
    """A keyframe selection test case."""

    case_id: str
    query: str
    scene_categories: list[str]
    target_object_ids: list[int]
    anchor_object_ids: list[int]
    expected_keyframe_indices: list[int]
    k_tolerance: int = 2
    description: str = ""


@dataclass
class HypothesisTestCase:
    """A hypothesis execution test case."""

    case_id: str
    query: str
    scene_categories: list[str]
    hypothesis_output: dict[str, Any]
    expected_status: str
    expected_selected_kind: str
    expected_matched_objects: list[int]
    description: str = ""


