def generate_all_ground_truth(output_dir: Path) -> None:
    """Generate all ground truth files."""
    output_dir.mkdir(parents=True, exist_ok=True)

    # Generate parsing test cases
    print("Generating parsing test cases...")
    parsing_cases = generate_parsing_test_cases()
    parsing_data = {
        "version": "1.0.0",
        "description": "Query parsing ground truth for migration equivalence testing",
        "scene_categories": SCENE_CATEGORIES,
        "cases": [asdict(case) for case in parsing_cases],
    }
    with open(output_dir / "parsing.json", "w") as f:
        json.dump(parsing_data, f, indent=2)
    print(f"  -> Generated {len(parsing_cases)} parsing test cases")

    # Generate keyframe test cases
    print("Generating keyframe test cases...")
    keyframe_cases = generate_keyframe_test_cases()
    keyframe_data = {
        "version": "1.0.0",
        "description": "Keyframe selection ground truth for migration equivalence testing",
        "cases": [asdict(case) for case in keyframe_cases],
    }
    with open(output_dir / "keyframes.json", "w") as f:
        json.dump(keyframe_data, f, indent=2)
    print(f"  -> Generated {len(keyframe_cases)} keyframe test cases")

    # Generate hypothesis test cases
    print("Generating hypothesis test cases...")
    hypothesis_cases = generate_hypothesis_test_cases()
    hypothesis_data = {
        "version": "1.0.0",
        "description": "Hypothesis execution ground truth for migration equivalence testing",
        "cases": [asdict(case) for case in hypothesis_cases],
    }
    with open(output_dir / "hypotheses.json", "w") as f:
        json.dump(hypothesis_data, f, indent=2)
    print(f"  -> Generated {len(hypothesis_cases)} hypothesis test cases")

    # Generate summary
    summary = {
        "generated_at": "2026-03-22",
        "counts": {
            "parsing": len(parsing_cases),
            "keyframes": len(keyframe_cases),
            "hypotheses": len(hypothesis_cases),
        },
        "files": ["parsing.json", "keyframes.json", "hypotheses.json"],
    }
    with open(output_dir / "manifest.json", "w") as f:
        json.dump(summary, f, indent=2)

    print(f"\nGround truth generation complete!")
    print(f"  Total cases: {sum(summary['counts'].values())}")
    print(f"  Output directory: {output_dir}")


def main() -> None:
    """Main entry point."""
    parser = argparse.ArgumentParser(
        description="Generate ground truth data for migration equivalence testing"
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=Path(__file__).parent.parent
        / "tests"
        / "migration"
        / "ground_truth",
        help="Output directory for ground truth files",
    )

    args = parser.parse_args()
    generate_all_ground_truth(args.output_dir)


if __name__ == "__main__":
    main()
